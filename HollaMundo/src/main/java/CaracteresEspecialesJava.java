public class CaracteresEspecialesJava {
    public static void main(String args[]){
    
        String nombre = "Carla";
        String apellido = "Perez";
        
        System.out.println(nombre + " " + apellido);
        
        System.out.println("Nueva linea \n " + nombre);
        
        System.out.println("Tabulador: \t" + apellido);
        
        System.out.println("Retroceder: \b" + apellido);
        
        System.out.println("Retorno de carro: \r" + apellido);
        /*se utiliza para el retorno de carro independientemente del sistema operativo*/
        System.lineSeparator();
        System.out.println("hola");
    
    }
    
}
